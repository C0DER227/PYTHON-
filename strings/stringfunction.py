#STRING FUNCTIONS
name='''Once upon a time
There lived a ghost
He was known to be a killer
And feared the most'''
print(len(name))         #length of string
print(name.endswith("t")) #words ending with required alphabet
print(name.find("to"))  #finds the required
print(name.replace("ghost","villan")) #replaces word to word
print(name.count("o"))  #counts the required
print(name.capitalize()) #converts small to capital only in the start